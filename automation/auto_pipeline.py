def cleanup():
    """Clean up temp files"""
    try:
        for f in TEMP_DIR.glob("*"):
            if f.is_file():
                f.unlink()
    except:
        pass

def run_daily():
    """Run the full pipeline"""
    print("\n" + "=" * 60)
    print(f"{CHANNEL_NAME} - PROFESSIONAL SHORT CREATOR")
    print("=" * 60)

    try:
        # 1. Generate script
        print("\n[1/5] Generating engaging script...")
        script = generate_engaging_script()
        print(f"  Topic: {script['topic']}")
        print(f"  Style: {script['style']}")

        # 2. Get video clip
        print("\n[2/5] Fetching video clip...")
        video_queries = [
            script["topic"].split()[0],
            script["topic"].split()[-1],
            script["style"],
            "nature", "abstract", "particles"
        ]

        video_path = None
        for query in video_queries:
            video_path = get_stock_video(query)
            if video_path and os.path.exists(video_path):
                print(f"  Got video: {video_path}")
                break

        if not video_path:
            raise Exception("Could not get video clip")

        # 3. Generate voiceover
        print("\n[3/5] Creating voiceover...")
        full_text = f"{script['hook']}. {script['intro']} {script['content']}"
        audio_path = generate_voiceover(full_text)

        if not audio_path or not os.path.exists(audio_path):
            raise Exception("Could not generate voiceover")

        print(f"  Voiceover ready")

        # 4. Create final video
        print("\n[4/5] Creating final video...")
        final_video = create_video_with_overlay(video_path, audio_path, script)

        if not final_video:
            raise Exception("Could not create video")

        # 5. Upload
        print("\n[5/5] Uploading to YouTube...")
        video_id = upload_as_short(
            final_video,
            script["title"],
            create_description(script),
            script["tags"]
        )

        # Save log
        log_file = BASE_DIR / "youtube" / "upload-log.json"
        log = json.loads(log_file.read_text()) if log_file.exists() else []
        log.append({
            "date": datetime.now().isoformat(),
            "topic": script["topic"],
            "style": script["style"],
            "video_id": video_id,
            "file": os.path.basename(final_video)
        })
        log_file.write_text(json.dumps(log, indent=2))

        print("\n" + "=" * 60)
        print(f"[+] SUCCESS! Video uploaded as Short!")
        if video_id:
            print(f"    https://www.youtube.com/shorts/{video_id}")
        print("=" * 60)

    except Exception as e:
        print(f"\n[x] Pipeline failed: {e}")

    finally:
        cleanup()


def create_video_with_overlay(video_path, audio_path, script):
    """Create final video with text overlay using FFmpeg"""
    output = UPLOADS_DIR / f"short-{datetime.now().strftime('%Y%m%d-%H%M%S')}.mp4"

    # Full script text for overlay
    hook_text = script["hook"][:80].replace("'", "").replace('"', "")
    intro_text = script["intro"][:60].replace("'", "").replace('"', "")

    # Build text overlay filter
    filter_str = f'''
        drawtext=text='{hook_text}':fontsize=36:fontcolor=white:borderw=3:bordercolor=black@0.8:x=(w-text_w)/2:y=h-300:enable='between(t,0,3)',
        drawtext=text='{intro_text}':fontsize=32:fontcolor=white:borderw=2:bordercolor=black@0.8:x=(w-text_w)/2:y=h-300:enable='between(t,3,6)',
        drawtext=text='{script["title"][:40]}...':fontsize=28:fontcolor=yellow:borderw=2:bordercolor=black@0.8:x=(w-text_w)/2:y=100:enable='between(t,6,45)'
    '''

    # Compose video with audio
    cmd = f'''ffmpeg -y -i "{video_path}" -i "{audio_path}" \
            -filter_complex "{filter_str}" \
            -map 0:v -map 1:a \
            -c:v libx264 -preset medium -crf 23 \
            -c:a aac -b:a 128k \
            -shortest \
            -t 50 \
            -r 30 \
            "{output}"'''

    print("  Creating video with text overlay...")
    result = os.system(cmd)

    if os.path.exists(output) and os.path.getsize(output) > 10000:
        print(f"  Video created: {output.name}")
        return str(output)

    return None

def upload_as_short(video_path, title, description, tags):
    """Upload video as YouTube Short"""
    try:
        import base64
        from google.oauth2.credentials import Credentials
        from google_auth_oauthlib.flow import InstalledAppFlow
        from googleapiclient.discovery import build
        from googleapiclient.http import MediaFileUpload

        # Setup credentials
        TOKEN_FILE = BASE_DIR / "youtube" / "token.json"
        CREDENTIALS_FILE = BASE_DIR / "youtube" / "credentials.json"

        # GitHub Actions secrets
        if os.environ.get("GITHUB_ACTIONS"):
            token_b64 = os.environ.get("YOUTUBE_TOKEN_DATA", "")
            creds_b64 = os.environ.get("YOUTUBE_CLIENT_CONFIG", "")

            if token_b64:
                TOKEN_FILE.write_text(json.dumps(json.loads(base64.b64decode(token_b64).decode())))
            if creds_b64:
                CREDENTIALS_FILE.write_text(json.dumps(json.loads(base64.b64decode(creds_b64).decode())))

        if not CREDENTIALS_FILE.exists():
            print("  [x] No credentials found")
            return None

        creds = Credentials.from_authorized_user_info(
            json.loads(TOKEN_FILE.read_text()),
            ["https://www.googleapis.com/auth/youtube.upload"]
        )

        youtube = build("youtube", "v3", credentials=creds)

        # For shorts, we upload as regular video but with shorts content
        # YouTube auto-detects vertical videos under 60 seconds as shorts
        body = {
            "snippet": {
                "title": f"SHORTS: {title}"[:100],
                "description": description[:5000],
                "tags": tags[:30],
                "categoryId": "22",  # People & Blogs
            },
            "status": {
                "privacyStatus": "public",
                "selfDeclaredMadeForKids": False,
            }
        }

        media = MediaFileUpload(video_path, chunksize=-1, resumable=True)
        request = youtube.videos().insert(
            part="snippet,status",
            body=body,
            media_body=media
        )

        print("  Uploading to YouTube...")
        response = None
        while response is None:
            status, response = request.next_chunk()

        video_id = response['id']
        print(f"  [+] Uploaded! https://www.youtube.com/shorts/{video_id}")
        return video_id

    except Exception as e:
        print(f"  [x] Upload failed: {e}")
        return None

